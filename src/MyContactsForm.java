    public void populateJtable() {
        ContactQuery cq = new ContactQuery();
        ArrayList<Contact> clist = cq.contactList(currentUserId);
        String[] colNames = {"Id", "First Name", "Last Name", "Group", "Phone", "Email", "Address", "Contact Photo"};
        Object[][] rows = new Object[clist.size()][8];

        for (int i = 0; i < clist.size(); i++) {
            rows[i][0] = clist.get(i).getCid();
            rows[i][1] = clist.get(i).getFname();
            rows[i][2] = clist.get(i).getLname();
            rows[i][3] = clist.get(i).getGroup();
            rows[i][4] = clist.get(i).getPhone();
            rows[i][5] = clist.get(i).getEmail();
            rows[i][6] = clist.get(i).getAddress();

            ImageIcon pic = new ImageIcon(new ImageIcon(clist.get(i).getPic()).getImage().getScaledInstance(70, 40, Image.SCALE_SMOOTH));

            rows[i][7] = pic;
            MyModel mmd = new MyModel(rows, colNames);
            tblDisplay.setModel(mmd);
            tblDisplay.setRowHeight(40);
            tblDisplay.getColumnModel().getColumn(7).setPreferredWidth(70);

        }
    }

